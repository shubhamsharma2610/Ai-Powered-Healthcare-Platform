import mongoose from 'mongoose';
import './User.js';
import './Patient.js';
import './Doctor.js';      // Doctor before Payment
import './Appointment.js'; // Appointment before Payment
const paymentSchema = new mongoose.Schema({
  appointmentId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Appointment',
    required: true
  },
  patientId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Patient',
    required: true
  },
  doctorId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Doctor',
    required: true
  },
  amount: {
    type: Number,
    required: true
  },
  razorpayOrderId: {
    type: String,
    required: true
  },
  razorpayPaymentId: {
    type: String
  },
  razorpaySignature: {
    type: String
  },
  status: {
    type: String,
    enum: ['created', 'success', 'failed'],
    default: 'created'
  }
}, { timestamps: true });

export default mongoose.model('Payment', paymentSchema);